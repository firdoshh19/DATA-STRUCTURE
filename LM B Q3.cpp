// 3. Write a program to calculate the average of three numbers entered by the user.
#include<iostream>
using namespace std;
int main (){
    int num1,num2,num3,average;

    cout<<"Enter first number : ";
    cin>>num1;

    cout<<"Enter second number : ";
    cin>>num2;

    cout<<"Enter third  number : ";
    cin>>num3;

    average=(num1+num2+num3)/3;

    cout<<"The average of three number : "<<average;

    return 0;





}
